    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        TCola colaValida1 = ColaClientesST.consultar();
        TCola colaValida2 = ColaClientesCT.consultar();
        if (!colaValida1.Vacia() | !colaValida2.Vacia()) {
            DefaultTableModel modelo = new DefaultTableModel();
            Cliente obj;
            TLista LC = Cliente.reporteD();

            modelo.addColumn("Nombre");
            modelo.addColumn("Edad");
            modelo.addColumn("Sexo");
            modelo.addColumn("Tipo de tarjeta");

            Object datos[] = new Object[4];

            for (int i = 0; i < LC.Cantidad(); i++) {
                obj = (Cliente) LC.Obtener(i);
                datos[0] = obj.getNombre();
                datos[1] = obj.getEdad();
                datos[2] = obj.getSexo();
                datos[3] = obj.getTipoTarjeta();
                modelo.addRow(datos);
            }
            tabla.setModel(modelo);
            if (LC.Vacia()) {
                JOptionPane.showMessageDialog(rootPane, "No se encontró ningún cliente");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "No existen clientes en ninguna de las colas por atender");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


