    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if (txtCodigo.getText().isEmpty() || txtNombre.getText().isEmpty() || txtEdad.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Porfavor llene los campos vacios");
        } else {
            String codigo = txtCodigo.getText();
            Escritor objEscritor = new Escritor();
            objEscritor.setCodEscritor(codigo);
            TArbolBin AB = new TArbolBin();
            AB = ArbolEscritores.obtener();
            TArbolBin ABB = AB.Buscar(objEscritor);
            
            if (ABB != null) {
                Escritor objEst = (Escritor) ABB.ObtenerDato();
                objEst.setNombre(txtNombre.getText());
                objEst.setEdad(Integer.parseInt(txtEdad.getText()));
                objEst.setSexo(boxSexo.getSelectedItem().toString());
                JOptionPane.showMessageDialog(this, "Escritor Reemplazado", "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
                Limpiar();
            } else {
                JOptionPane.showMessageDialog(this, "Escritor no Encontrado", "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
    private void Limpiar() {
        txtCodigo.setText("");
        txtNombre.setText("");
        txtEdad.setText("");
        boxSexo.setSelectedIndex(0);
    }
    private void txtEdadKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtEdadKeyTyped
        char character = evt.getKeyChar();
        if (!isDigit(character)) {
            evt.consume();
        }

    }//GEN-LAST:event_txtEdadKeyTyped


