    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Cliente obj = new Cliente();
        
        obj.setNombre(txtNombre.getText());
        obj.setEdad(Integer.parseInt(txtEdad.getText()));
        obj.setSexo(cboSexo.getSelectedItem().toString());
        
        String tipoTarjeta = cboTipoTarjeta.getSelectedItem().toString();
        obj.setTipoTarjeta(tipoTarjeta);
        
        if (tipoTarjeta.equalsIgnoreCase("Ninguna")) {
            ColaClientesST.adicionar(obj);
        } else {
            ColaClientesCT.adicionar(obj);
        }
        
        txtNombre.setText("");
        txtEdad.setText("");
        JOptionPane.showMessageDialog(rootPane, "Cliente registrado");
    }//GEN-LAST:event_jButton1ActionPerformed


