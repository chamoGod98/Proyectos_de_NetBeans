    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Cliente obj = new Cliente();
        
        if (cboCola.getSelectedIndex()==0) {
            TCola cola1 = ColaClientesST.consultar();
            obj=(Cliente) cola1.Primero();
            lblNombre.setText(obj.getNombre());
            lblTarjeta.setText(obj.getTipoTarjeta());
        } else {
            TCola cola2 = ColaClientesCT.consultar();
            obj=(Cliente) cola2.Primero();
            lblNombre.setText(obj.getNombre());
            lblTarjeta.setText(obj.getTipoTarjeta());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Cliente obj = new Cliente();
        
        if (cboCola.getSelectedIndex()==0) {
            TCola colaST = ColaClientesST.consultar();
            float monto = Float.parseFloat(lblMonto.getText());
            obj=(Cliente)colaST.Eliminar();
            obj.setNumEntradas(Integer.parseInt(txtEntradas.getText()));
            obj.setTotalPagado(monto);
            ListaClientesAtendidosST.adicionar(obj);
            lblNombre.setText("");
            lblTarjeta.setText("");
            txtEntradas.setText("");
            lblMonto.setText("");
            JOptionPane.showMessageDialog(rootPane, "Cliente atendido");
        } else {
            TCola colaCT = ColaClientesST.consultar();
            float monto = Float.parseFloat(lblMonto.getText());
            obj=(Cliente)colaCT.Eliminar();
            obj.setNumEntradas(Integer.parseInt(txtEntradas.getText()));
            obj.setTotalPagado(monto);
            ListaClientesAtendidosST.adicionar(obj);
            lblNombre.setText("");
            lblTarjeta.setText("");
            txtEntradas.setText("");
            lblMonto.setText("");
            JOptionPane.showMessageDialog(rootPane, "Cliente atendido");
        }
    }//GEN-LAST:event_jButton3ActionPerformed


