    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("Nombre de proyecto");
        modelo.addColumn("Costo");
        
        Object datos[] = new Object[2];
        
        TLista LE = null;
        try {
            LE = Proyecto.ProyectoMayorCosto();
        } catch (Exception ex) {
            Logger.getLogger(FrmReporteC.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        for (int i = 0; i < LE.Cantidad(); i++) {
            Proyecto obj = (Proyecto) LE.Obtener(i);
            datos[0]=obj.getNombre();
            datos[1]=obj.getCosto();
            modelo.addRow(datos);
        }
        tblReporteC.setModel(modelo);
    }//GEN-LAST:event_btnMostrarActionPerformed


