    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        
        Participante objParticipante = new Participante();
        
        String nombre = txtNombre.getText();
        String sexo = cboSexo.getSelectedItem().toString();
        String tipoParticipante = cboTipoParticipante.getSelectedItem().toString();
        String univProcedencia = cboUniversidadProcedencia.getSelectedItem().toString();
        int costoParticipante = Integer.parseInt(txtCostoParticipante.getText());
        
        objParticipante.setNombre(nombre);
        objParticipante.setSexo(sexo);
        objParticipante.setTipoParticipante(tipoParticipante);
        objParticipante.setUnivProcedencia(univProcedencia);
        objParticipante.setCostoParticipante(costoParticipante);
        
        Lista_Participantes.registrar(objParticipante);
        
        txtNombre.setText("");
        cboSexo.setSelectedIndex(-1);
        cboTipoParticipante.setSelectedIndex(-1);
        cboUniversidadProcedencia.setSelectedIndex(-1);
        txtCostoParticipante.setText("");
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

