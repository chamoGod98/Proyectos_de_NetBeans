    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        Lista_Combustibles LC = new Lista_Combustibles();
        String codComb = txtCodigo.getText();
        int pos = LC.buscarCodComb(codComb);

        if (pos!=-1) {
            JOptionPane.showMessageDialog(rootPane, "Este combustible ya está registrado", "ERROR", 2);
        } else {
            Combustible obj = new Combustible();
            obj.setNombre(txtNombre.getText());
            obj.setCodigo(txtCodigo.getText());
            obj.setNumGalones(Float.parseFloat(txtNumGalones.getText()));
            obj.setPrecio(Double.parseDouble(txtPrecio.getText()));
            obj.setTipo(cboTipo.getSelectedItem().toString());
            obj.setEstado(txtEstado.getText());

            Lista_Combustibles.adicionar(obj);
            JOptionPane.showMessageDialog(rootPane, "Combustible registrado", "ÉXITO", 1);
            txtNombre.setText("");
            txtCodigo.setText("");
            txtNumGalones.setText("");
            txtPrecio.setText("");
            btnRegistrar.setEnabled(false);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void txtCodigoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoKeyTyped
        char c = evt.getKeyChar();

        if ((c < '0' | c > '9') | txtCodigo.getText().length() >= 4) {
            evt.consume();
        }
    }//GEN-LAST:event_txtCodigoKeyTyped

