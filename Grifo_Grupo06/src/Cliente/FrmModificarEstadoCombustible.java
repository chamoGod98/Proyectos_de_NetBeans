    private void btnVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerActionPerformed
        if (!LC.Vacia()) {
            String codigo = cboCodigo.getSelectedItem().toString();
            for (int i = 0; i < LC.Cantidad(); i++) {
                Combustible obj = (Combustible) LC.Obtener(i);
                if (codigo == obj.getCodigo()) {
                    obj = (Combustible) LC.Obtener(i);
                    txtNombre.setText(obj.getNombre());
                    txtTipo.setText(obj.getTipo());
                    txtNumGalones.setText(String.valueOf(obj.getNumGalones()));
                    txtPrecio.setText(String.valueOf(obj.getPrecio()));
                    if (obj.getEstado().equalsIgnoreCase("DISPONIBLE")) {
                        btnDarDeBaja.setEnabled(true);
                    } else {
                        btnDarDeAlta.setEnabled(true);
                    }
                    break;
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "No hay ningún combustible registrado", "AVISO", 2);
        }

    }//GEN-LAST:event_btnVerActionPerformed

    private void btnDarDeBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDarDeBajaActionPerformed
        String codigo = cboCodigo.getSelectedItem().toString();
        for (int i = 0; i < LC.Cantidad(); i++) {
            Combustible obj = (Combustible) LC.Obtener(i);
            if (codigo == obj.getCodigo()) {
                int rpta = JOptionPane.showConfirmDialog(rootPane, "¿Dar de baja?", "Confirmar", 0, 3);
                if (rpta == 0) {
                    obj = (Combustible) LC.Obtener(i);
                    obj.setEstado("NO DISPONIBLE");
                    JOptionPane.showMessageDialog(rootPane, "Estado modificado", "ÉXITO", 1);
                    btnDarDeBaja.setEnabled(false);
                }
            }
        }
    }//GEN-LAST:event_btnDarDeBajaActionPerformed

