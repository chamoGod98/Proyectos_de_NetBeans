    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!LC.Vacia()) {
            String codigo = cboCodigo.getSelectedItem().toString();
            for (int i = 0; i < LC.Cantidad(); i++) {
                Combustible obj = (Combustible) LC.Obtener(i);
                if (codigo == obj.getCodigo()) {
                    obj = (Combustible) LC.Obtener(i);
                    txtNombre.setText(obj.getNombre());
                    txtTipo.setText(obj.getTipo());
                    txtNumGalones.setText(String.valueOf(obj.getNumGalones()));
                    txtPrecio.setText(String.valueOf(obj.getPrecio()));
                    btnEliminar.setEnabled(true);
                    break;
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "No hay ningún combustible registrado", "AVISO", 2);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
            int rpta = JOptionPane.showConfirmDialog(rootPane, "¿Modificar estado?", "Confirmar", 0, 3);
            if (rpta == 0) {
                Lista_Combustibles LComb = new Lista_Combustibles();
                String codComb = cboCodigo.getSelectedItem().toString();
                int pos = LComb.buscarCodComb(codComb);
                LC.Eliminar(pos);
                btnEliminar.setEnabled(false);
            }
    }//GEN-LAST:event_btnEliminarActionPerformed


