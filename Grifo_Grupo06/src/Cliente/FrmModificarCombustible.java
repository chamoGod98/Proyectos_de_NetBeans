    private void btnVerCombustibleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerCombustibleActionPerformed
        if (!LC.Vacia()) { //Validar que haya combustibles registrados
            String codigo = cboCodigo.getSelectedItem().toString();
            for (int i = 0; i < LC.Cantidad(); i++) {
                Combustible obj = (Combustible) LC.Obtener(i);
                if (codigo == obj.getCodigo()) {
                    obj = (Combustible) LC.Obtener(i);
                    txtNombre.setText(obj.getNombre());
                    txtTipo.setText(obj.getTipo());
                    txtNumGalones.setText(String.valueOf(obj.getNumGalones()));
                    txtPrecio.setText(String.valueOf(obj.getPrecio()));
                    break;
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "No hay ningún combustible disponible", "AVISO", 2);
        }
        btnGuardar.setEnabled(true);
    }//GEN-LAST:event_btnVerCombustibleActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        String codigo = cboCodigo.getSelectedItem().toString();
        for (int i = 0; i < LC.Cantidad(); i++) {
            Combustible obj = (Combustible) LC.Obtener(i);
            if (codigo == obj.getCodigo()) {
                int rpta = JOptionPane.showConfirmDialog(rootPane, "¿Modificar estado?", "Confirmar", 0, 3);
                if (rpta == 0) {
                    obj = (Combustible) LC.Obtener(i);
                    obj.setNombre(txtNombre.getText());
                    obj.setTipo(txtTipo.getText());
                    obj.setNumGalones(Float.parseFloat(txtNumGalones.getText()));
                    obj.setPrecio(Double.parseDouble(txtPrecio.getText()));
                    JOptionPane.showMessageDialog(rootPane, "Cambios guardados", "ÉXITO", 1);
                    txtNombre.setText("");
                    txtTipo.setText("");
                    txtNumGalones.setText("");
                    txtPrecio.setText("");
                    txtNombre.setEnabled(false);
                    txtTipo.setEnabled(false);
                    txtNumGalones.setEnabled(false);
                    txtPrecio.setEnabled(false);
                    btnGuardar.setEnabled(false);
                }
            }
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

