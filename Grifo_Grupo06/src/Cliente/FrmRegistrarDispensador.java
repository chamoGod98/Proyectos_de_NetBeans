    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        Lista_Dispensadores LD = new Lista_Dispensadores();
        int numDisp = Integer.parseInt(txtNumDisp.getText());
        int pos = LD.buscarNumDisp(numDisp);

        if (pos != -1) {
            JOptionPane.showMessageDialog(rootPane, "Este dispensador ya está registrado", "ERROR", 2);
        } else {
            Dispensador obj = new Dispensador();

            obj.setCodigoTrabajador(Integer.parseInt(cboCodigo.getSelectedItem().toString()));
            obj.setNumero(Integer.parseInt(txtNumDisp.getText()));
            obj.setNumeroCombustibles(Integer.parseInt(txtNumComb.getText()));
            obj.setEstado(txtEstado.getText());

            Lista_Dispensadores.adicionar(obj);
            JOptionPane.showMessageDialog(rootPane, "Dispensador registrado", "En horabuena", 1);
            cboCodigo.setSelectedIndex(0);
            txtNumDisp.setText("");
            txtNumComb.setText("");
            txtEstado.setText("");
            
            btnRegistrar.setEnabled(false);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void txtNumDispKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNumDispKeyTyped
        char c = evt.getKeyChar();

        if ((c < '0' | c > '9') | txtNumDisp.getText().length() >= 1) {
            evt.consume();
        }
    }//GEN-LAST:event_txtNumDispKeyTyped

