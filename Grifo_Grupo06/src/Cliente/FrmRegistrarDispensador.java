    public void habilitarBtnRegistrar(){
        String numD = txtNumDisp.getText();
        String numC = txtNumComb.getText();
        if (numD.length()==1 & numC.length()==1 & !LT.Vacia()) {
            btnRegistrar.setEnabled(true);
        } else {
            btnRegistrar.setEnabled(false);
        }
    }

