    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String codigo = cboCodigo.getSelectedItem().toString();
        for (int i = 0; i < LT.Cantidad(); i++) {
            Trabajador obj = (Trabajador) LT.Obtener(i);
            if (codigo == obj.getCodigo()) {
                int rpta = JOptionPane.showConfirmDialog(rootPane, "¿Modificar estado?", "Confirmar", 0, 3);
                if (rpta == 0) {
                    obj = (Trabajador) LT.Obtener(i);
                    JOptionPane.showMessageDialog(rootPane, "Cambios guardados", "ÉXITO", 1);
                    obj.setNombre(txtNombres.getText());
                    obj.setApellido(txtApellidos.getText());
                    obj.setNumCelular(Integer.parseInt(txtNumCelular.getText()));
                    obj.setCorreo(txtCorreo.getText());
                    txtNombres.setText("");
                    txtNombres.setEnabled(false);
                    txtApellidos.setText("");
                    txtApellidos.setEnabled(false);
                    txtNumCelular.setText("");
                    txtNumCelular.setEnabled(false);
                    txtCorreo.setText("");
                    txtCorreo.setEnabled(false);
                    btnGuardar.setEnabled(false);
                }
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


