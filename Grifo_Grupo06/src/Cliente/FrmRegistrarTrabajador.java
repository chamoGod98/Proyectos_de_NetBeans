    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        Lista_Trabajadores LT = new Lista_Trabajadores();
        String codTrab = txtCodigo.getText();
        int pos = LT.buscarCodTrab(codTrab);

        Trabajador obj = new Trabajador();
        if (pos != -1) {
            JOptionPane.showMessageDialog(rootPane, "Trabajador ya registrado. Intente nuevamente", "ERROR", 2);
        } else {
            obj.setNombre(txtNombres.getText());
            obj.setApellido(txtApellidos.getText());
            obj.setCodigo(txtCodigo.getText());
            obj.setSexo(cboSexo.getSelectedItem().toString());
            obj.setNumCelular(Integer.parseInt(txtNumCelular.getText()));
            obj.setCorreo(txtCorreo.getText());
            obj.setEstado(txtEstado.getText());

            Lista_Trabajadores.adicionar(obj);
            JOptionPane.showMessageDialog(rootPane, "Trabajador registrado", "ÉXITO", 1);

            txtNombres.setText("");
            txtApellidos.setText("");
            txtCodigo.setText("");
            txtNumCelular.setText("");
            txtCorreo.setText("");
            txtEstado.setText("");
            
            btnRegistrar.setEnabled(false);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

