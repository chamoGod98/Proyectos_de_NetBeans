    private void btnVerAutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerAutoActionPerformed
        Auto obj = new Auto();
        Pila pilaAutos = PilaAutos.consultar();
        if (!pilaAutos.esVacia()) {
            obj = (Auto) pilaAutos.peek();
            txtPlaca.setText(obj.getNumPlaca());
            txtProp.setText(obj.getPropietario());
            btnVerAuto.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(rootPane, "No hay ningún auto registrado");
        }

    }//GEN-LAST:event_btnVerAutoActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (!txtPlaca.getText().isEmpty() && !txtProp.getText().isEmpty() && !txtHoras.getText().isEmpty() && !txtMinutos.getText().isEmpty()) {
            Pila pilaAutos = PilaAutos.consultar();
            if (!pilaAutos.esVacia()) {
                Auto obj = (Auto) pilaAutos.pop(); //posición 0, último auto registrado
                obj.setHoraSalida(Integer.parseInt(txtHoras.getText()));
                obj.setMinutosSalida(Integer.parseInt(txtMinutos.getText()));
                ColaSalidaAutos.adicionar(obj);
                JOptionPane.showMessageDialog(rootPane, "Salida programada");
                btnVerAuto.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Error. Pila vacía");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un auto");
        }

    }//GEN-LAST:event_jButton2ActionPerformed


