    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Cliente obj = new Cliente();

        TLista lista = ListadoGeneral.consultar();

        if (lista.Vacia()) {
            JOptionPane.showMessageDialog(rootPane, "No hay clientes");
        } else {
            DefaultTableModel modelo = new DefaultTableModel();

            modelo.addColumn("Nombre");
            modelo.addColumn("Edad");
            modelo.addColumn("Categoría");
            modelo.addColumn("Sexo");
            modelo.addColumn("Película");
            modelo.addColumn("Costo de entrada");

            Object datos[] = new Object[6];

            for (int i = 0; i < lista.Cantidad(); i++) {
                obj = (Cliente) lista.Obtener(i);
                datos[0] = obj.getNombre();
                datos[1] = obj.getEdad();
                datos[2] = obj.getCategoria();
                datos[3] = obj.getSexo();
                datos[4] = obj.getPelicula();
                datos[5] = obj.getCostoEntrada();
                modelo.addRow(datos);
            }
            tabla.setModel(modelo);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


