    public void listar() {
        DefaultTableModel modelo = new DefaultTableModel();
        
        Object datos[] = new Object[3];
        
        modelo.addColumn("Código");
        modelo.addColumn("Descripción");
        modelo.addColumn("Tipo de clima");
        
        TLista lista = Destino.ConsultarTodo();
        
        for (int i = 0; i < lista.Cantidad(); i++) {
            Destino objD = (Destino) lista.Obtener(i);
            datos[0]=objD.getCodigo();
            datos[1]=objD.getDescripcion();
            datos[2]=objD.getTipoClima();
            modelo.addRow(datos);
        }
        jTable1.setModel(modelo);
    }

