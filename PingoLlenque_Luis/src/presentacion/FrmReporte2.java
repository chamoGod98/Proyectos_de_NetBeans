    private void btnVerPartidosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerPartidosActionPerformed
        
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("Rival");
        modelo.addColumn("Día");
        modelo.addColumn("Mes");
        modelo.addColumn("Año");
        modelo.addColumn("Goles a favor");
        modelo.addColumn("Goles en contra");
        modelo.addColumn("Tipo de competición");
        modelo.addColumn("Ciudad");
        
        Object datos[] = new Object[8];
        
        String tipoCompetencia = cboTipoComp.getSelectedItem().toString();
        
        ArrayList listaPorTipoCompetencia = partidos.Reporte2(tipoCompetencia);
        
        for (int i = 0; i < listaPorTipoCompetencia.size(); i++) {
            partidos objPartido = (partidos) listaPorTipoCompetencia.get(i);
            datos[0]=objPartido.getRival();
            datos[1]=objPartido.getDia_fec();
            datos[2]=objPartido.getMes_fec();
            datos[3]=objPartido.getAño_fec();
            datos[4]=objPartido.getGoles_a_favor();
            datos[5]=objPartido.getGoles_en_contra();
            datos[6]=objPartido.getTipo_competicion();
            datos[7]=objPartido.getCiudad();
            modelo.addRow(datos);
        }
        tblDatos.setModel(modelo);
        
    }//GEN-LAST:event_btnVerPartidosActionPerformed


