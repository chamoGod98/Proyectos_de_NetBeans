    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel modelo = new DefaultTableModel();
        String sexo = cboSexo.getSelectedItem().toString();
        
        modelo.addColumn("Codigo");
        modelo.addColumn("Nombre");
        modelo.addColumn("Edad");
        modelo.addColumn("Sexo");
        modelo.addColumn("Ciclo");
        
        Object datos[] = new Object[5];
        
        ArrayList lista = Alumno.consultarSexo(sexo);
        
        for (int i = 0; i < lista.size(); i++) {
            Alumno objAlumno = (Alumno) lista.get(i);
            datos[0]=objAlumno.getNombre();
            datos[1]=objAlumno.getCodigo();
            datos[2]=objAlumno.getEdad();
            datos[3]=objAlumno.getSexo();
            datos[4]=objAlumno.getCiclo();
            modelo.addRow(datos);
        }
        tblDatosPorSexo.setModel(modelo);
    }//GEN-LAST:event_jButton1ActionPerformed


