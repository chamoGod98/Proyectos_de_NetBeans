    private void btnRegistrarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarProductoActionPerformed
        
        Producto objProd = new Producto();

        String codigo = txtCodigo.getText();
        String nombreProducto = txtNombreProducto.getText();
        String tipoProducto = cboTipo.getSelectedItem().toString();
        float cantProduccion = Float.parseFloat(txtCantProduccion.getText());
        int lote = Integer.parseInt(cboLote.getSelectedItem().toString());
        float costoPorKg = Float.parseFloat(txtCosto.getText());
        
        objProd.setNombreProducto(nombreProducto);
        objProd.setCodigo(codigo);
        objProd.setCantProduccion(cantProduccion);
        objProd.setLote(lote);
        objProd.setCostoPorKg(costoPorKg);
        objProd.setTipoProducto(tipoProducto);
        
        Lista_Productos.adicionar(objProd);
        
        txtCodigo.setText("");
        txtNombreProducto.setText("");
        cboTipo.setSelectedIndex(0);
        txtCantProduccion.setText("");
        cboLote.setSelectedIndex(0);
        txtCosto.setText("");
        
    }//GEN-LAST:event_btnRegistrarProductoActionPerformed


